
public class NutritionInfo {
	public int calories, protein, fat, carbs, sugars, calcium, potassium, sodium, vitA, vitC;
	public String[] allFields = new String[]{
		"calories", "protein", "fat", "carbs", "sugars", "calcium", "potassium", "sodium", "vitA", "vitC"
	};
	public int getCalories() {
		return calories;
	}
	public void setCalories(int calories) {
		this.calories = calories;
	}
	public int getProtein() {
		return protein;
	}
	public void setProtein(int protein) {
		this.protein = protein;
	}
	public int getFat() {
		return fat;
	}
	public void setFat(int fat) {
		this.fat = fat;
	}
	public int getCarbs() {
		return carbs;
	}
	public void setCarbs(int carbs) {
		this.carbs = carbs;
	}
	public int getSugars() {
		return sugars;
	}
	public void setSugars(int sugars) {
		this.sugars = sugars;
	}
	public int getCalcium() {
		return calcium;
	}
	public void setCalcium(int calcium) {
		this.calcium = calcium;
	}
	public int getPotassium() {
		return potassium;
	}
	public void setPotassium(int potassium) {
		this.potassium = potassium;
	}
	public int getSodium() {
		return sodium;
	}
	public void setSodium(int sodium) {
		this.sodium = sodium;
	}
	public int getVitA() {
		return vitA;
	}
	public void setVitA(int vitA) {
		this.vitA = vitA;
	}
	public int getVitC() {
		return vitC;
	}
	public void setVitC(int vitC) {
		this.vitC = vitC;
	}
	
}
